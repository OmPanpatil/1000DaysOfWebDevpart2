public class day81_5 {
    public static void main(String[] args) {
        /*
        Type Casting or Explicit or Narrowing
        to make one data type change into another with forcing by data loss is type casting

        int a = 20;
        float b = (int)a
        
        type promotion converts byte, short, char into int 

        and long, float and double into their respective type.
        */
        float a = 20;
        int b = (int)a;
        System.out.println(b);
    }
}
